# Mod Contributor Network

## Overview
This document outlines the structure and expectations for contributors to the RimWorld mod.

## How to Contribute
- **Code Contributions:** Fork our repository, make changes, and submit a pull request.
- **Testing Contributions:** Participate in testing pre-release versions and provide feedback.
- **Design Contributions:** Submit designs and ideas for new features and improvements.

## Contributor Expectations
- Adhere to our code of conduct and contribution guidelines.
- Engage positively with the community and other contributors.
- Strive for quality and innovation in contributions.
